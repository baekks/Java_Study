package homework;

/*
    'num이 1부터 10이 될 때까지 num값을 증가하며 반복한다'를 의미하는 for문을 완성해 보세요.
            초기화식;       조건식;        증감식
    for (                                           ) { ... }
 */
public class Q4_12 {
    public static void main(String[] args) {
        for (int num=0; num<=10; num++) {
            System.out.println("1~10까지" + num);
        }
    }
}
